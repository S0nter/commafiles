### PROMPT ############################################################################################################################################################################################
prompt()
{
	exitcode=$?
	test $exitcode -eq 0 && exitcode="" || exitcode="\[\e[1;31m\]$? "
	export PS1="$exitcode\[\e[38;5;51m\]\u\[\e[38;5;255m\]@\[\e[38;5;244m\]\h \[\e[38;5;255m\]\w > "
}
PROMPT_COMMAND="prompt"

### COMPLETION ########################################################################################################################################################################################
bind TAB:menu-complete
# set completion-ignore-case On

